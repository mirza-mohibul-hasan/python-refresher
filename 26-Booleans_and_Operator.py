# Comparison Operator
print(1 == 1) # Equal
print(1 != 1) # Not equal
print(1 > 2) # Greater than
print(2 >= 1) # Greater than or equal
print(1 < 5) # Less than
print(1 <= 7) # Less than or equal

# Logical Operator
print(2 > 1 and 5 < 7) # Both need to be true
print(2 >= 1 or 5 < 7) # One need to be true
print(not(1 == 1)) # Opposite of Condition