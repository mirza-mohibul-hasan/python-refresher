# Flow Control: If Elif Else

# If Statement
age = 1
if age < 3:
    print("Baby") # Automatic added 4 spaces which is called indent or something.

# If Else
is_boy = False
if is_boy:
    print("Boy")
else:
    print("Girl")

# Elif
hour = 16
if hour < 12:
    print("Good Morning")
elif hour >= 12 and hour <= 17:
    print("Good Afternoon")
else:
    print("Good Evening")