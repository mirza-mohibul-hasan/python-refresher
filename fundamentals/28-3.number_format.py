number = 3.14159
decimal = 10000

print("The number pi is {:.2f}". format(number))
print("The number pi is in position {:,}". format(decimal))
print("The number pi is in binary {:b}". format(decimal))
print("The number pi is in octal {:o}". format(decimal))
print("The number pi is in hexa {:X}". format(decimal))
print("The number pi is in scintific {:E}". format(decimal))